package com.company;

public class While_Loop {
    public static void main(String[] args) {
        int i=1;

        //Print 1 to 10
        while (i<=10){
            System.out.println(i);
            i++;
        }

        //Print 1 to 100
        while(i<=100){
            System.out.println(i);
            i++;
        }
    }
}
